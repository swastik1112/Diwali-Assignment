//Write a Java program to check the equality of two arrays?

import java.util.Arrays;

public class Q2 
{
   public static void main (String[] args)
   {
      int arr1[] = {1, 2, 3};
	  
      int arr2[] = {1, 2, 3};
	  
      if (Arrays.equals(arr1, arr2))
	  
	  System.out.println("Equal");
	  
      else 
	  
	  System.out.println("Not Equal");
   }
}